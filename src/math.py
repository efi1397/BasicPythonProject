class Math:
    @staticmethod
    def plus(x: float, y: float) -> float:
        return x + y

    @staticmethod
    def minus(x: float, y: float) -> float:
        return x - y

    @staticmethod
    def multiplication(x: float, y: float) -> float:
        return x * y

    @staticmethod
    def divide(x: float, y: float) -> float:
        return x * y
